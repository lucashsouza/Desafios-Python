algo = input ('Digite algo e veja as informações correspondentes: ')
print('É um digito?', algo.isdigit())
print('É alfanumerico?', algo.isalnum())
print('É um numero?', algo.isnumeric())
print('É decimal?', algo.isdecimal())
print('É espaço?', algo.isspace())
print('É uma letra?', algo.isalpha())
print('É um titulo?', algo.istitle())
print('Texto em maiscula?', algo.isupper())
print('Texto em minuscula?', algo.islower())
print('É um identificador?', algo.isidentifier())
print('Pode ser printado?', algo.isprintable())


